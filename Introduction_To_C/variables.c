#include <stdio.h>

/**
* @brief Demonstration of variables and declaration.
*
* @return 
*/
int main() {
    // in programming input and data are stored in variables, there are several different
    // types of variables, when you tell the compiler you are declaring a variable,
    // you must include the data type along with the name of the variable

    // before you can use a variable you must tell the compiler about it, what "type"
    // it is, variable syntax is as follows <variable type> <name of variable>;
    int myVariable;

    // notice a semi colon is always used at the end of a expression
    // you can also declare multiple variables of the same type on the same line
    // all you need is to separate by comma
    int a,b,c,d,e,f;
    char letter;
    float the_float, the_last_float;

    return 0;
}
